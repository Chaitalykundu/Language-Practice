#include<iostream>
using namespace std;
int main()
{
    int a=25, b=10, add, sub, mult, div, mod;
    add=a+b;
    sub=a-b;
    mult=a*b;
    div=a/b;
    mod=a%b;
    cout << "Addition is: " << add << endl;
    cout << "Subtraction is: " << sub << endl;
    cout << "Multiplication is: " << mult << endl;
    cout << "division is: " << div << endl;
    cout << "modulo division is: " << mod << endl;
}
